                },
                // toolbar: [
                //     'heading', '|', 'bulletedList', 'numberedList', 'alignment', 'undo', 'redo'
                // ]
            })
            .catch(error => {
                console.error(error);
            });

        const deleteBlogButton = document.querySelectorAll('.delete-blog-button');

        // Define a click event handler for each button
        deleteBlogButton.forEach(function(button) {
            button.addEventListener('click', function() {
                // Find the 'delete-link' data attribute of the clicked button
                var deleteLink = this.getAttribute('data-delete-link');
                // Set the 'action' attribute of the form with id 'delete-blog-form'
                var deleteForm = document.getElementById('delete-blog-form');
                deleteForm.setAttribute('action', deleteLink);
            });
        });
    })
</script>
